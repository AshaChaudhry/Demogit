public class PositiveZeroNagative {
    public static void main(String[] args) {
        float f = -22.55f;
        if (f > 0) {
            System.out.println("number is Positive");
        } else if (f == 0) {
            System.out.println("number is Zero");
        } else {
            System.out.println("number is Nagative");
        }
    }// output is Nagative
}
