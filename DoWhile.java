package com.homework;

public class DoWhile {
    public static void main(String[] args) {
        int i=0;
        do{
            System.out.println(i);
            i+=2;
        }while(i<=20);
        //prints between 0-20 even numbers
//--------------------------------------------------------
        int j=1;
        do{
            System.out.println(j);
            j+=2;
        }while(j<=20);
        //prints odd numbers between 1-20
        //------------------------------------------------------
        int num=13;
        int k=1;
        do{
            System.out.println(num+"x"+k+"="+num*k);
            ++k;
        }while(k<=10);

//prints 13 time tables




    }







}
