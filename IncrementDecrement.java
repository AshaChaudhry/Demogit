package com.homework;

public class IncrementDecrement {
    public static void main(String[] args) {
        int a=10;
        System.out.println(a++);//print 10
        System.out.println(a++);//print 11
        System.out.println(a++);//print 12
        System.out.println(a++);//print 13
        System.out.println(a++);//print 14
        System.out.println(a++);//print 15
        System.out.println(a++);//print 16
        System.out.println(a++);//print 17
        System.out.println(a++);//print 18
        System.out.println(a++);//print 19
        System.out.println(a++);//print 20 last number should be printed 21
// decrements
        System.out.println("------------");
        System.out.println(a--);// prints 20
        System.out.println(a--);//prints 19
        System.out.println(a--);//prints 18
        System.out.println(a--);//prints 17
        System.out.println(a--);//prints 16
        System.out.println(a--);//prints 15
        System.out.println(a--);//prints 14
        System.out.println(a--);//prints 13
        System.out.println(a--);//prints 12
        System.out.println(a--);//last number should be printed 12

    }
}
